fruits = ["apple", "banana", "cherry"]
for x in fruits:
  if x == "banana":
    continue
  print(x)

for i in range(5):
    if i == 3:
        continue
    print(i)

for letter in "Python":
    if letter == "o":
        continue
    print(letter)

numbers = [1, 2, 3, 4, 5]
for n in numbers:
    if n % 2 == 0:
        continue
    print(n)

